'use strict';
var test = require('tape')
var leVal = require('../lib/le-val')
var hex = require('../lib/hexstring')

test('\nlittle endian value', function (t) {
  
  [ [ [ 0x00, 0x00, 0x00, 0x00 ], 0x00000000 /* (           0) */ ]
  , [ [ 0x01, 0x00, 0x00, 0x00 ], 0x00000001 /* (           1) */ ]
  , [ [ 0xff, 0x00, 0x00, 0x00 ], 0x000000ff /* (         255) */ ]
  , [ [ 0x00, 0x01, 0x00, 0x00 ], 0x00000100 /* (         256) */ ]
  , [ [ 0x01, 0x01, 0x00, 0x00 ], 0x00000101 /* (         257) */ ]
  , [ [ 0xff, 0x01, 0x00, 0x00 ], 0x000001ff /* (         511) */ ]
  , [ [ 0xff, 0xff, 0x00, 0x00 ], 0x0000ffff /* (      65,535) */ ]
  , [ [ 0x00, 0x00, 0xff, 0x00 ], 0x00ff0000 /* (  16,711,680) */ ]
  , [ [ 0xff, 0xff, 0xff, 0x00 ], 0x00ffffff /* (  16,777,215) */ ]
  , [ [ 0x00, 0x00, 0x00, 0x0f ], 0x0f000000 /* (4,026,531,840)*/ ]
  , [ [ 0x00, 0x00, 0x00, 0xf0 ], 0xf0000000 /* (4,026,531,840) */ ]
  , [ [ 0x00, 0x00, 0x00, 0xff ], 0xff000000 /* (- 16,777,216) */ ]
  , [ [ 0xff, 0xff, 0xff, 0xff ], 0xffffffff /* (-          1) */ ]
  ].forEach(runTest)

  function runTest(c) {
    t.equal(leVal(c[0]), c[1], 'leval(' + c[0].map(hex) + ') === ' + hex(c[1]))
  }
  t.end()
})
